print("Welcome to calculator!")
num1 = int(input("Enter num #1 "))
num2 = int(input ("Enter num #2 "))
operation = input("Add, subtract, multiply or divide?")
if operation == "add":
    print(num1 + num2)
if operation == "subtract":
    print(num1 - num2)
if operation == "multiply":
    print(num1 * num2)
if operation == "divide":
    print(num1 / num2)


